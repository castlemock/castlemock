package com.castlemock.core.basis.model;

/**
 * The ServiceResult is a container class for the output message generated by the service classes.
 * @author Karl Dahlgren
 * @since 1.0
 */
public class ServiceResult<O extends Output> {

    private O output;

    /**
     * Default constructor for the ServiceResult
     * @param output The output message generated by a service class
     */
    public ServiceResult(O output) {
        this.output = output;
    }

    /**
     * Get the output message
     * @return The output message
     */
    public O getOutput() {
        return output;
    }

    /**
     * Sets a new value for the output message
     * @param output The new output message that will replace the old one
     */
    public void setOutput(O output) {
        this.output = output;
    }
}
